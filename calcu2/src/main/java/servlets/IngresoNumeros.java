        PrintWriter out = response.getWriter();
        int nu1 = Integer.parseInt(request.getParameter("n1"));
        int nu2 = Integer.parseInt(request.getParameter("n2"));
        
        Calculo cl = new Calculo(nu1, nu2);
        
